#include "libft.h"

void	ft_del_twodem_arr(char ***p_arr)
{
	int i;

	if (!p_arr)
		return ;
	i = 0;
	while (*((*p_arr) + i))
		free(*((*p_arr) + i++));
	free(*p_arr);
	*p_arr = NULL;
}
